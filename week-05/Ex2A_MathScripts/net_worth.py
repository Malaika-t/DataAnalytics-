# Assets
checking_account = 5000
saving_account = 20000
car = 25000
investments = 8000

total_assets = checking_account + saving_account + car + investments 

# Debts
credit_card = 2000
car_loan = 4000
mortgage = 50000

total_debts = credit_card + car_loan + mortgage

# Net Worth
net_worth = total_assets - total_debts 

# Output
print(f"Your total assets are {total_assets}")
print(f"Your total debts are {total_debts}")
print(f"Your net worth is {net_worth}")